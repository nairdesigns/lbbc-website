                    <div class="col-md-12">
                        <div class="articleList">
							<div class="col-md-6">
								<ul id="accordion">
									<h6>Of the Scriptures</h6>
                                    <div class="accordion-content">
                                        <p>We believe that the Holy Bible was written by men supernaturally inspired; that it has truth without any admixture of error for its matter; and therefore is, and shall remain to the end of the age, the only complete and final revelation of the will of God to man; the true center of Christian union and the supreme standard by which all human conduct, creeds, and opinions should be tried.</p>
                                        <p>1. By "The Holy Bible" we mean that collection of sixty-six books, from Genesis to Revelation, which, as originally written does not only contain and convey the Word of God, but IS the very Word of God.</p>
                                        <p>2. By "inspiration" we mean that the books of the Bible were written by holy men of old, as they were moved by the Holy Spirit, in such a definite way that their writings were supernaturally and verbally inspired and free from error, as no other writings have ever been or ever will be inspired.</p>
                                        <p class="reference">Psalm 19:7-11; 119:89,105,130,160; Proverbs 30:5-6; Isaiah 8:20; Luke 16:31; 24:25-27,44-45; John 5:39,45-47; 12:48; 17:17; Acts 1:16; 28:25; Romans 3:4; 15:4; Ephesians 6:17; 2Timothy 3:16-17; 1Peter 1:23; 2Peter 1:19-21; Revelation 22:19</p>
                                    </div>

									<h6>Of the True God</h6>
                                    <div class="accordion-content">
                                        <p>We believe that there is one, and only one, living and true God, an infinite, intelligent Spirit, the maker and supreme ruler of heaven and earth; inexpressibly glorious in holiness and worthy of all possible honor, confidence and love; that in the unity of the Godhead there are three persons, the Father, the Son and the Holy Ghost, equal in every divine perfection, and executing distinct but harmonious offices in the great work of redemption.</p>
                                        <p class="reference">Genesis 17:1; Exodus 20:2-3; 15:11; Psalm 83:18; 90:2; 147:5; Jeremiah 10:10; Matthew 28:19; Mark 12:30; John 4:24; 10:30; 15:26; 17:5; Acts 5:3-4; Romans 11:23; 1Corinthians 12:10-11; 8:6; 12:4-6; 2Corinthians 13:14; Ephesians 2:18; 4:6; Philippians 2:5-6; 1Timothy 1:17; 1John 5:7; Revelation 4:11</p>
                                    </div>

									<h6>Of the Holy Spirit</h6>
                                    <div class="accordion-content">
                                        <p>We believe that the Holy Spirit is a divine person; equal with God the Father and God the Son and of the same nature; that He was active in the creation; that in His relation to the unbelieving world He restrains the evil one until God's purpose is fulfilled; that He convicts of sin, of judgment and of righteousness; that He bears witness to the Truth of the Gospel in preaching and testimony; that He is the agent in the New Birth; that He seals, endues, guides, teaches, witnesses, sanctifies and helps the believer.</p>

                                        <p class="reference">Genesis 1:13; Matthew 3:11; 28:19; Mark 1:8; Luke 1:35; 3:16; 24:49; John 1:33; 3:5-6; 14:16-17,26; 15:26-27; 16:8-11,13; Acts 5:30-32; 11:16; Romans 8:14,16,26-27; Ephesians 1:13-14; 2Thessalonians 2:7,13; Hebrews 9:14; 1Peter 1:2</p>
                                    </div>

									<h6>Of the Devil, or Satan</h6>
                                    <div class="accordion-content">
                                        <p>We believe that Satan was once holy, and enjoyed heavenly honors; but through pride and ambition to be as the Almighty, fell and drew after him a host of angels; that he is now the malignant prince of the power of the air, and the unholy god of this world. We hold him to be man's great tempter, the enemy of God and His Christ, the accuser of the saints, the author of all false religions, the chief power back of the present apostasy; the lord of the antichrist, and the author of all the powers of darkness - destined however to final defeat at the hands of God's own Son, and to the judgment of an eternal justice in hell, a place prepared for him and his angels.</p>

                                        <p class="reference">Isaiah 14:12-15; Ezekiel 28:14-17; Matthew 4:1-3; 13:25; 25:41; 27:39; Mark 13:21-22; Luke 22:3-4; John 14:30; Ephesians 2:2; 2Corinthians 11:13-15; 1Thessalonians 3:5; 2Thessalonians 2:8-11; 1Peter 5:8; 2Peter 2:4; 1John 2:22; 3:8; 4:3; 2John 7; Jude 6; Revelation 12:7-10; 13:13-14; 19:11,16,20; 20:1-3,10</p>
                                    </div>

									<h6>Of the Creation</h6>
                                    <div class="accordion-content">
                                        <p>We believe in the Genesis account of creation and that it is to be accepted literally, and not allegorically or figuratively; that man was created directly in God's own image and after His own likeness; that man's creation was not a matter of evolution or evolutionary change of species, or development through interminable periods of time from lower to higher forms; that all animal and vegetable life was made directly and God's established law was that they should bring for only "after their kind." Since God created mankind into genders distinctly male and female, we believe that the only legitimate marriage is that between a man and a woman. He has commanded that no intimate sexual activity be engaged in outside of marriage. We believe that any form of homosexuality, lesbianism, bisexuality, bestiality, incest, fornication, adultery and pornography are sinful perversions of God's gift of sex. We believe that God rejects any attempt to alter one's gender by surgery or appearance.</p>

                                        <p class="reference">Genesis 1:1,11,24,26-27; 2:21-23; Exodus 20:11; Nehemiah 9:6; Jeremiah 10:12; John 1:3; Acts 4:24; 17:23-26; Romans 1:20; Colossians 1:16-17; Hebrews 11:3; Revelation 10:6</p>
                                    </div>

									<h6>Of the Fall of Man</h6>
                                    <div class="accordion-content">
                                        <p>We believe that man was created in innocence under the law of his Maker, but by voluntary transgression fell from his sinless and happy state, in consequence of which all mankind are now sinners, not by constraint, but of choice; and therefore under just condemnation without defense or excuse.</p>

                                        <p class="reference">Genesis 3:1-6,24; Ezekiel 18:19-20; Romans 1:18,20,28,32; 3:10-19; 5:12,19; Galatians 3:22; Ephesians 2:1,3</p>
                                    </div>

									<h6>Of the Virgin Birth</h6>
                                    <div class="accordion-content">
                                        <p>We believe that Jesus Christ was begotten of the Holy Ghost in a miraculous manner; born of Mary, a virgin, as no other man was ever born or can ever be born of a woman, and that He is both the Son of God, and God, the Son.</p>

                                        <p class="reference">Genesis 3:15; Psalm 2:7; Isaiah 7:14; Matthews 1:18-25; Mark 1:1; Luke 1:35; John 1:14; Galatians 4:4; 1Corinthians 15:47; 1John 5:20</p>
                                    </div>

									<h6>Of the Atonement for Sin</h6>
                                    <div class="accordion-content">
                                        <p>We believe that the salvation of sinners is wholly of grace; through the mediatorial offices of the Son of God, Who by appointment of the Father, freely took upon Him our nature, yet without sin, honored the divine law by His personal obedience, and by His death made a full and vicarious atonement for our sins; that His atonement consisted not in setting us an example by His death as a martyr, but was the voluntary substitution of Himself in the sinner's place, the Just dying for the unjust, Christ, the Lord, bearing our sins in His own body on the tree; that, having risen from the dead, He is now enthroned in heaven and uniting in His wonderful person the tenderest sympathies with divine perfection, He is in every way qualified to be a suitable, a compassionate and an all sufficient Savior.</p>

                                        <p class="reference">Isaiah 53:4-7,11-12; Matthew 18:11; John 3:16; 10:18; Acts 15:11; Romans 3:24-25; Galatians 1:4; Ephesians 2:8; Philippians 2:7-8; Hebrews 2:14; 7:25; 9:12-15; 12:2</p>
                                    </div>

									<h6>Of Grace in the New Creation</h6>
                                    <div class="accordion-content">
                                        <p>We believe that in order to be saved, sinners must be born again; that the new birth is a new creation in Christ Jesus; that it is instantaneous and not a process; that in the new birth the one dead in trespasses and in sins is made a partaker of the divine nature and receives eternal life, the free gift of God; that the new creation is brought about in a manner above our comprehension, not by culture, not by character, nor by the will of man, but wholly and solely by the power of the Holy Spirit in connection with divine truth, so as to secure our voluntary obedience to the gospel; that its proper evidence appears in the holy fruits of repentance and faith and newness of life.</p>

                                        <p class="reference">Luke 5:27; John 1:12-13; 3:3,6-7; Acts 2:41; Romans 6:23; 2Corinthians 5:17,19; Galatians 5:22; Ephesians 2:1; 5:9; Colossians 2:13; 1John 5:1</p>
                                    </div>

									<h6>Of the Freeness of Salvation</h6>
                                    <div class="accordion-content">
                                        <p>We believe in God's electing grace; that the blessings of salvation are made free to all by the gospel; that it is the immediate duty of all to accept them by a cordial, penitent and obedient faith; and nothing prevents the salvation of the greatest sinner on earth but his own inherent depravity and voluntary rejection of the gospel; which rejection involves him in an aggravated condemnation.</p>

                                        <p class="reference">Isaiah 55:1; Matthew 11:28; John 3:15-18,36; 5:40; 6:37; Acts 2:38; Romans 8:29-30; 10:13; 1Corinthians 15:10; Ephesians 2:4-5; Colossians 3:12; 1Thessalonians 1:4; 1Timothy 1:15; Titus 1:1; 1Peter 1:2; Revelation 22:17</p>
                                    </div>

								</ul>
							</div>
							<div class="col-md-6">
								<ul id="accordion2">
									<h6>Of Justification</h6>
                                    <div class="accordion-content">
                                        <p>We believe that the great gospel blessing which Christ secures to such as believe in Him is Justification; that Justification includes the pardon of sin, and the gift of eternal life on principles of righteousness; that it is bestowed not in consideration of any works of righteousness which we have done; but solely through faith in the Redeemer's blood, His righteousness is imputed unto us.</p>

                                        <p class="reference">Isaiah 53:11; Habakkuk 2:4; Zechariah 13:1; Acts 13:39; Romans 1:17; 4:1; 5:1-9; 8:1; Galatians 3:11; Titus 3:5-7; Hebrews 10:38</p>
                                    </div>

									<h6>Of Repentance and Faith</h6>
                                    <div class="accordion-content">
                                        <p>We believe that Repentance and Faith are solemn obligations, and also inseparable graces, wrought in our souls by the quickening Spirit of God; thereby, being deeply convicted of our guilt, danger and helplessness, and of the way of salvation by Christ, we turn to God with unfeigned contrition, confession and supplication for mercy; at the same time heartily receiving the Lord Jesus Christ and openly confessing Him as our only and all-sufficient Savior.</p>
                                        <p class="reference">Psalm 51:1-4,7; Isaiah 55:6-7; Mark 1:15; Luke 12:8; 18:13; Acts 2:37-38; 20:21; Romans 10:9-11,13</p>
                                    </div>

									<h6>Of the Church</h6>
                                    <div class="accordion-content">
                                        <p>We believe that a Baptist church is a congregation of baptized believers associated by a covenant of faith and fellowship of the Gospel, said church being understood to be the citadel and propagator of the divine and eternal grace; observing the ordinances of Christ; governed by His laws; exercising the gifts, rights, and privileges invested in them by His Word; that its officers of ordination are pastors or elders whose qualifications, claims, and duties are clearly defined in the scriptures.</p>

                                        <p>We believe that men and women are spiritually equal in position before God but that God has ordained distinct and separate spiritual functions of men and women in the home and in the church. The husband is to be the leader of the home and men are to hold the leadership positions (pastors and deacons) in the church. Accordingly, only men are eligible of licensure and ordination for pastor by the church.</p>

                                        <p>We believe the true mission of the church is found in the Great Commission: first, to make individual disciples; second, to build up the church; third, to teach and instruct as He has commanded. We do not believe in the reversal of this order; we hold that the local church has the absolute right of self government, free from the interference of any hierarchy of individuals or organizations; and that the one and only superintendent is Christ through the Holy Spirit; that it is scriptural for true churches to cooperate with each other in contending for the faith and for the furtherance of the Gospel; that every church is the sole and only judge of the measure and method of its cooperation; on all matters of membership, of policy, of government, of discipline, of benevolence, the will of the local church is final.</p>
                                        <p class="reference">Leviticus 27:31; Malachi 3:10; Matthew 28:19-20; Acts 2:41-42; 6:5-6; 14:23; 15:22-23; 20:17-28; 1Corinthians 5:11-13; 6:1-3; 11:2; 12:4,8-11; 16:1-2; Ephesians 1:22-23; 4:11; 5:23-24; Colossians 1:18; 1Timothy 3:1-13</p>
                                    </div>

									<h6>Of Baptism and the Lord's Supper</h6>
                                    <div class="accordion-content">
                                        <p>We believe that Christian baptism is the immersion in water of a believer, in the name of the Father, of the Son, and of the Holy Ghost, with the authority of the local church, to show forth in a solemn and beautiful emblem our faith in the crucified, buried and risen Savior, with its effect in our death to sin and resurrection to a new life; that it is prerequisite to the privileges of a church relation and to the Lord's supper, in which the members of the church, by the sacred use of bread and the fruit of the vine are to commemorate together the dying love of Christ; preceded always by solemn self-examination.</p>
                                        <p class="reference">Matthew 3:6; 3:16; 28:19-20; John 3:23; Acts 2:41-42; 8:36-39; Romans 6:3-5; 1Corinthians 11:23-28; Colossians 2:12</p>
                                    </div>

									<h6>Of the Perseverance of the Saints</h6>
                                    <div class="accordion-content">
                                        <p>We believe that such only are real believers as endure unto the end; that their persevering attachment to Christ is the grand mark which distinguishes them from superficial professors; that a special Providence watches over their welfare; and that they are kept by the power of God through faith unto eternal salvation.</p>
                                        <p class="reference">Psalm 121:3; Matthew 6:20; 13:19-21; John 8:31-32; 10:28-29; 16:8; Romans 8:28,35-39; Philippians 1:6; Colossians 1:21-23; Hebrews 1:14; 1Peter 1:5; 1John 2:19</p>
                                    </div>

									<h6>Of the Righteous and the Wicked</h6>
                                    <div class="accordion-content">
                                        <p>We believe that there is a radical and essential difference between the righteous and the wicked; that such only as through faith are justified in the name of the Lord Jesus, and sanctified by the Spirit of our God, are truly righteous in His esteem; while all such as continue in impenitence and unbelief are in His sight wicked, and under the curse, and this distinction holds among men both in and after death, in the everlasting felicity of the saved and the everlasting conscious suffering of the lost.</p>
                                        <p class="reference">Genesis 18:23; Proverbs 11:31; 14:32; Malachi 3:18; Matthew 7:13-14; 25:34; Luke 9:26; 16:25; John 8:21; 12:25; Acts 10:34-35; Romans 1:17; 6:16-18,23; 7:6; 1Corinthians 15:22; Galatians 3:10; 1Peter 4:18; 1John 2:7,29; 5:19</p>
                                    </div>

									<h6>Of Civil Government</h6>
                                    <div class="accordion-content">
                                        <p>We believe that civil government is of divine appointment, for the interests and good order of human society; that magistrates are to be prayed for, conscientiously honored and obeyed; except only in things opposed to the will of our Lord Jesus Christ; who is the only Lord of the conscience, and the coming Prince of the kings of the earth.</p>
                                        <p class="reference">Exodus 18:21-22; 2Samuel 23:3; Psalm 72:11; Daniel 3:17-18; Matthew 10:28; 22:21; 23:10; Acts 4:19-20; 23:5; Romans 13:7; Philippians 2:10-11; Titus 3:1; 1Peter 2:13-14,17</p>
                                    </div>

									<h6>Of the Resurrection, Return of Christ & Related Events</h6>
                                    <div class="accordion-content">
                                        <p>We believe in and accept the sacred Scriptures upon these subjects at their face and full value. Of the Resurrection, we believe that Christ rose bodily "The third day according to the Scriptures"; that He ascended "to the right hand of the throne of God"; that He alone is our "merciful and faithful high priest in things pertaining to God"; "that this same Jesus which is taken up from you into heaven shall so come in like manner as ye have seen Him go into heaven," bodily, personally, and visibly; that the "dead in Christ shall rise first"; that the living saints "shall all be changed in a moment, in the twinkling of an eye, at the last trump"; "that the Lord God shall give unto Him the throne of His father David"; and that "Christ shall reign a thousand years in righteousness until He hath put all enemies under His feet."</p>
                                        <p class="reference">Psalm 72:8; Isaiah 11:4-5; Matthew 24:27,42; Matthew 28:6-7; Mark 16:6,19; Luke 1:32; Luke 24:2,4-6,39,51; John 14:3; John 20:27; Acts 1:9,11; 1Corinthians 15:4; Philippians 4:20; 1Thessalonians 4:16; 1Timothy 2:5; Hebrews 2:17; 5:9; 8:1; 9:28; 12:2</p>
                                    </div>

									<h6>Of Missions</h6>
                                    <div class="accordion-content">
                                        <p>The command to give the gospel to the world is clear and unmistakable and this Commission was given to the churches.</p>

                                        <p class="reference">Matthew 28:18-20, "And Jesus came and spake unto them, saying All power is given unto me in heaven and in earth. Go ye therefore, and teach all nations, baptizing them in the name of the Father, and of the Son, and of the Holy Ghost: Teaching them to observe all things whatsoever I have commanded you and, lo I am with you alway, even unto the end of the world. Amen."</p>

                                        <p class="reference">Mark 16:15, "And he said unto them, Go ye into all the world and preach the gospel to every creature."</p>

                                        <p class="reference">John 20:21, "As my Father hath sent me, even so send I you."</p>

                                        <p class="reference">Acts 1:8, "But ye shall receive power, after that the Holy Ghost is come upon you; and ye shall be witnesses unto me both in Jerusalem, and in all Judea, and in Samaria, and unto the uttermost part of the earth."</p>

                                        <p class="reference">Romans 10:13-15, "For whosoever shall call upon the name of the Lord shall be saved. How then shall they call on him in whom they have not believed? And how shall they believe in him of whom they have not heard? And how shall they hear without a preacher? And how shall they preach, except they be sent? As it is written, How beautiful are the feet of them that preach the gospel of peace, and bring glad tidings of good things!"</p>
                                    </div>

									<h6>Of the Grace of Giving</h6>
