            <div class="container">
				<h4>Articles of Faith</h4>
                <div class="container960">
                    <div class="row">
                        <div class="col-md-12">
                            <div class="text-left">
                                <p>A Bible Baptist is one who believes in a supernatural Bible, which tells of a supernatural Christ, Who had a supernatural birth, Who spoke supernatural words, Who performed supernatural miracles, Who lived a supernatural life, Who died a supernatural death, Who rose in supernatural power, Who ascended in supernatural splendor, Who intercedes as a supernatural Priest and Who will one day return in supernatural glory to establish a supernatural kingdom on the earth.</p>
